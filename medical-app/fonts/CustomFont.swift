import UIKit

extension UIFont {

    static func Regular(size: CGFloat) -> UIFont {
        return UIFont(name: "NunitoSans-Regular", size: size)!
    }
    
    static func Bold(size: CGFloat) -> UIFont {
        return UIFont(name: "NunitoSans-Bold", size: size)!
    }
}
